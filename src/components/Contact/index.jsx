          <MapContainer center={[30.320692, 78.084378]} zoom={13}>
            <TileLayer url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png" />
            <Marker position={[30.320692, 78.084378]}>
              <Popup>Ujjwal lives here, come over for a cup of coffee :)</Popup>
            </Marker>
          </MapContainer>
